export default function wordifyNum(num){
  if (num === 3) {
     return 'three'
  }else if (num === 4) {
     return 'four'
  }else if (num === 5) {
     return 'five'
  }else if (num === 6) {
     return 'six'
  }else if (num === 7) {
     return 'seven'
  }else if (num === 8) {
     return 'eight'
  }
}
